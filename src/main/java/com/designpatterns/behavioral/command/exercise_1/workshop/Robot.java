package com.designpatterns.behavioral.command.exercise_1.workshop;

public class Robot {

    public void turnOn() {
        System.out.println("Robot has been turned on");
    }

    public void cut() {
        System.out.println("Robot is cutting");
    }

    public void drill() {
        System.out.println("Robot is drilling");
    }

    public void turnOff() {
        System.out.println("Robot has been turned off");
    }

}
